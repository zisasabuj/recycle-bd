// scripts/gen-handlers-registry.js
// Generates api_handlers/_registry.js — a single ESM module that
// statically imports every handler. The Vercel bundler only
// includes files it sees referenced via import(), and the dynamic
// `${HANDLERS_DIR}${route.file}` template string in the catch-all
// is invisible to the bundler. This registry fixes that by giving
// every handler a static import.

import { readdirSync, statSync, writeFileSync, readFileSync } from 'fs';
import { join, relative } from 'path';

const ROOT = new URL('../api_handlers/', import.meta.url).pathname;
const OUT = new URL('../api_handlers/_registry.js', import.meta.url).pathname;

function walk(dir, out = []) {
  for (const name of readdirSync(dir)) {
    const full = join(dir, name);
    if (statSync(full).isDirectory()) walk(full, out);
    else if (name.endsWith('.js') && !name.startsWith('_')) out.push(full);
  }
  return out;
}

const files = walk(ROOT).sort();
let counter = 0;
const imports = [];
const map = [];

for (const full of files) {
  const rel = relative(ROOT, full).replace(/\\/g, '/');
  // Safe var name: strip .js, replace / and - and [ and ] with _
  const safe = rel.replace(/\.js$/, '').replace(/[\/\-\[\]]/g, '_');
  const varName = `h_${counter++}_${safe}`;
  imports.push(`import ${varName} from './${rel}';`);
  map.push(`  '${rel.replace(/\.js$/, '')}': ${varName},`);
}

const content = `// AUTO-GENERATED by scripts/gen-handlers-registry.js
// Static imports of every handler file. This forces the Vercel
// bundler to include them all in the function bundle.

${imports.join('\n')}

const HANDLERS = {
${map.join('\n')}
};

export default HANDLERS;
`;

writeFileSync(OUT, content);
console.log('Wrote', OUT);
console.log('Total handlers:', files.length);
